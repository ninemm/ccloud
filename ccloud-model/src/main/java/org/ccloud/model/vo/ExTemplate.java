package org.ccloud.model.vo;

public class ExTemplate {
	
	private String activityExecteId;
	private String activityExecteTemplateId;
	private String templateValue;
	private String templateValueType;
	private String templateValueTypeValue;
	public String getActivityExecteId() {
		return activityExecteId;
	}
	public void setActivityExecteId(String activityExecteId) {
		this.activityExecteId = activityExecteId;
	}
	public String getTemplateValue() {
		return templateValue;
	}
	public void setTemplateValue(String templateValue) {
		this.templateValue = templateValue;
	}
	public String getTemplateValueType() {
		return templateValueType;
	}
	public void setTemplateValueType(String templateValueType) {
		this.templateValueType = templateValueType;
	}
	public String getTemplateValueTypeValue() {
		return templateValueTypeValue;
	}
	public void setTemplateValueTypeValue(String templateValueTypeValue) {
		this.templateValueTypeValue = templateValueTypeValue;
	}
	public String getActivityExecteTemplateId() {
		return activityExecteTemplateId;
	}
	public void setActivityExecteTemplateId(String activityExecteTemplateId) {
		this.activityExecteTemplateId = activityExecteTemplateId;
	}
	
}
