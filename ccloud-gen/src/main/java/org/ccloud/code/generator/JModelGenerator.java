package org.ccloud.code.generator;

import com.jfinal.plugin.activerecord.generator.ModelGenerator;
import com.jfinal.plugin.activerecord.generator.TableMeta;

public class JModelGenerator extends ModelGenerator {

	public JModelGenerator(String modelPackageName,
			String baseModelPackageName, String modelOutputDir) {
		super(modelPackageName, baseModelPackageName, modelOutputDir);
		
		this.packageTemplate = "/**%n"
				+ " * Copyright (c) 2015-2016, Eric Huang 黄鑫 (hx50859042@gmail.com).%n"
				+ " *%n"
				+ " * Licensed under the GNU Lesser General Public License (LGPL) ,Version 3.0 (the \"License\");%n"
				+ " * you may not use this file except in compliance with the License.%n"
				+ " * You may obtain a copy of the License at%n"
				+ " *%n"
				+ " *      http://www.gnu.org/licenses/lgpl-3.0.txt%n"
				+ " *%n"
				+ " * Unless required by applicable law or agreed to in writing, software%n"
				+ " * distributed under the License is distributed on an \"AS IS\" BASIS,%n"
				+ " * WITHOUT WARRANTIES OR CONDITIONS OF ANY KIND, either express or implied.%n"
				+ " * See the License for the specific language governing permissions and%n"
				+ " * limitations under the License.%n"
				+ " */%n"
				+ "package %s;%n%n";
		
		this.importTemplate = "import org.ccloud.model.core.Table;%n"
				+ "import %s.%s;%n%n";
		
		this.classDefineTemplate =
				"/**%n" +
				" * Generated by 九毫米(http://9mm.tech).%n" +
				" */%n" +
				"@Table(tableName=\"%s\",primaryKey=\"%s\")%n" +
				"public class %s extends %s<%s> {%n";
		
		
		this.daoTemplate = "%n\tprivate static final long serialVersionUID = 1L;%n%n";
		
	}
	
	@Override
	protected void genClassDefine(TableMeta tableMeta, StringBuilder ret) {
		ret.append(String.format(classDefineTemplate, tableMeta.name,
			tableMeta.primaryKey, tableMeta.modelName, tableMeta.baseModelName, tableMeta.modelName));
	}

	@Override
	protected void genDao(TableMeta tableMeta, StringBuilder ret) {
		ret.append(String.format(daoTemplate));
	}

}
