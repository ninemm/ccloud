@RouterMapping(url = "/admin/department", viewPath = "/WEB-INF/admin/department")
@Before(ActionCacheClearInterceptor.class)
@RouterNotAllowConvert
public class _DepartmentController extends JBaseCRUDController<Department> { 
	
	public void list() {
		
		String keyword = getPara("k", "");
		if (StrKit.notBlank(keyword)) {
			keyword = StringUtils.urlDecode(keyword);
			setAttr("k", keyword);
		}
		
		String parentId = getPara("parentId", "0");
		
		Page<Department> page = DepartmentQuery.me().paginate(getPageNumber(), getPageSize(), parentId, keyword, null);
		Map<String, Object> map = ImmutableMap.of("total", page.getTotalRow(), "rows", page.getList());
		renderJson(map);
		
	}
	
	@Override
	public void edit() {
		String id = getPara("id");
		if (id != null) {
			setAttr("dept", DepartmentQuery.me().findById(id));
		}

		String templateHtml = "admin_user_edit.html";
		if (TemplateManager.me().existsFile(templateHtml)) {
			setAttr("include", TemplateManager.me().currentTemplatePath() + "/" + templateHtml);
			return;
		}

	}

	@Override
	public void save() {
		
		final Department dept = getModel(Department.class);

		if (StringUtils.isBlank(dept.getDeptName())) {
			renderAjaxResultForError("部门名称不能为空");
			return;
		}
		if (dept.getDeptLevel() == null) {
			renderAjaxResultForError("部门等级不能为空");
			return;
		}
		if (dept.getPrincipalUserId() == null) {
			renderAjaxResultForError("部门负责人不能为空");
			return;
		}
		
		if (dept.getId() == null) {
			dept.setCreateDate(new Date());
		}else {
			dept.setModifyDate(new Date());
		}
		
		dept.setIsParent(0);
		if (dept.saveOrUpdate()) {
			renderAjaxResultForSuccess("ok");
		} else {
			renderAjaxResultForError("false");
		}
	}

	@Override
	public void delete() {
		String id = getPara("id");
		final Department r = DepartmentQuery.me().findById(id);
		if (r != null) {
			if (r.delete()) {
				renderAjaxResultForSuccess();
				return;
			}
		}
		renderAjaxResultForError();
	}
	
	public void batchDelete() {
		String [] ids = getParaValues("dataItem");
		int count = DepartmentQuery.me().batchDelete(ids);
		if (count > 0) {
			renderAjaxResultForSuccess("success");
		} else {
			renderAjaxResultForError("batch delete error!");
		}
	}
	
	public void department_tree() {
		List<Map<String, Object>> list = DepartmentQuery.me().findDeptListAsTree(1);
		setAttr("treeData", JSON.toJSON(list));
	}
	
}
